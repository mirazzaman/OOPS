package com.Operators;

public class Arithmatic {

	public static void main(String[] args) {
		// Declear variables
		int a = 10; 
		int b = 6;
		
		 // addition operator
		System.out.println("Arithmatic operation for (a+b)= "+ (a+b));
		
		 // Substactions operator
		System.out.println("Arithmatic operation for (a-b)= "+ (a-b));
		
		// Multiplicaton operator
		System.out.println("Arithmatic operation for (a*b)= "+ (a*b));
				
		 // Divisio operator
		System.out.println("Arithmatic operation for (a/b)= "+ (a/b));
		
		// Modulas operator
		System.out.println("Arithmatic operation for (a%b)= "+ (a%b));		
	}
}
