package com.Operators;

public class Assignment {

	public static void main(String[] args) {
		//Create variable 
		int a = 4;
		int val;
		
		// assign value using =
		
		val = a;	
		System.out.println("val using = : " + val);
		
	    // assign value using =+
	    val += a;
	    System.out.println("var using +=: " + val);
	    
	    // assign value using =*
	    val *= a;
	    System.out.println("var using *=: " + val);
	    



	    

	}
}
