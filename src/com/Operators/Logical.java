package com.Operators;

public class Logical {
	public static void main(String[] args) {
		
		System.out.println("Using && : "+((30>10)&&(40<60)));
		System.out.println("Using && : "+((30>10)&&(40<30)));
		
		System.out.println("Using && : "+((300>10)||(40>60)));
		System.out.println("Using && : "+(!(30>155)||(40!=30)));
		
		
		
	}

}
