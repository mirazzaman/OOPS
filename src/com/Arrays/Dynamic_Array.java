package com.Arrays;

public class Dynamic_Array {

	public static void main(String[] args) {
		// Declearing array
		int arr[];
		//Initialized array 
		arr = new int[5]; // 0, 1, 2, 3 
		
		//adding value to each index
		
		arr[0]= 10;
		arr[1]= 20;
		arr[2]= 30;
		arr[3]= 40;
		arr[4]= 50;
		System.out.print("total values are : ");
		for(int i =0; i<arr.length; i++) {
			System.out.print(arr[i]+", ");
		}
		
		
		
		
		
		
		

	}

}
