package com.Control_Statement;

public class ForLoops {
	public static void main(String[] args) {
		System.out.println("The Increment Value of I is : ");
		int totalValue = 10;
		for(int i=1 ; i<=totalValue; i++) {
			System.out.print(i+", ");
		}
		System.out.println("\n");
		System.out.println("The Decrement Value of J is : ");
		
		for(int j =totalValue; j>=0; j--) {
			System.out.print(j+", ");
		}
		
		
		
		
	}

}
